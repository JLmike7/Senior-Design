	mBrickTexSRV = mTexMgr.CreateTexture(L"Textures/bricks.dds");
	mStoneNormalTexSRV = mTexMgr.CreateTexture(L"Textures/floor_nmap.dds");
	mBrickNormalTexSRV = mTexMgr.CreateTexture(L"Textures/bricks_nmap.dds");

